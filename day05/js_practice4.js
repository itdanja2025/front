if( 가장큰수 < 두번째값 ){ 가장큰수 = 두번째값; } // 만약에 두번째값이 가장큰수 보다 크면 대입/수정
if( 가장큰수 < 세번째값 ){ 가장큰수 = 세번째값; } // !!!! : elseif 대신에 다중 if 처리 
console.log( 가장큰수 ); // 변수 활용 : (1) [입력받은/중간연산식] 저장 (2) 가독성 

